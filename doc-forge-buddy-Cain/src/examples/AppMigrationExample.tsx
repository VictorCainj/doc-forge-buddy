import { Suspense, lazy, useEffect } from 'react';
import { Toaster } from '@/components/ui/toaster';
import { Toaster as Sonner } from '@/components/ui/sonner';
import { TooltipProvider } from '@/components/ui/tooltip';
import { ToastProvider } from '@/components/ui/toast-notification';
import { BrowserRouter, Routes, Route, useLocation } from 'react-router-dom';
import { AnimatePresence, motion } from 'framer-motion';

// ‚úÖ NOVO: Usar o sistema unificado de providers
import { AppProviders } from '@/providers';

// ‚úÖ NOVO: Usar hooks do novo sistema
import { useGlobalState, useAppInitialization, useUserPreferences } from '@/hooks/useAppStore';

// Importa√ß√µes legadas (para migra√ß√£o gradual)
import ProtectedRoute from '@/components/ProtectedRoute';
import AdminRoute from '@/components/AdminRoute';
import Layout from '@/components/layout';
import PageLoader from '@/components/common/PageLoader';
import ErrorBoundary from '@/components/common/ErrorBoundary';
import PageTransition from '@/components/common/PageTransition';
import PerformanceMonitor from '@/components/PerformanceMonitor';
import { prefetchRouteModules, getPrefetchMetrics } from '@/utils/prefetchRoutes';
import { setNotificationQueryClient } from '@/features/notifications/utils/notificationAutoCreator';
import { useBehaviorBasedLoading, preloadCriticalLibs } from '@/hooks/useSmartImport';

// Lazy load de p√°ginas para code splitting
const Index = lazy(() => import('./pages/Index'));
const Login = lazy(() => import('./pages/Login'));
const Contratos = lazy(() => import('./pages/Contratos'));
const ForgotPassword = lazy(() => import('./pages/ForgotPassword'));
const NotFound = lazy(() => import('./pages/NotFound'));
// ... outras p√°ginas

// ‚úÖ NOVO: Configura√ß√£o do QueryClient agora est√° no AppProviders
// Removido de c√° para evitar duplica√ß√£o

// Componentes de renderiza√ß√£o (mantidos como estavam)
const renderPublic = (element: JSX.Element) => (
  <PageTransition>{element}</PageTransition>
);

const renderPublicAuth = (element: JSX.Element) => (
  <PageTransition>
    <ProtectedRoute requireAuth={false}>{element}</ProtectedRoute>
  </PageTransition>
);

const renderProtected = (element: JSX.Element) => (
  <PageTransition>
    <ProtectedRoute>
      <Layout>{element}</Layout>
    </ProtectedRoute>
  </PageTransition>
);

const renderAdmin = (element: JSX.Element) => (
  <PageTransition>
    <ProtectedRoute>
      <AdminRoute>
        <Layout>{element}</Layout>
      </AdminRoute>
    </ProtectedRoute>
  </PageTransition>
);

// ‚úÖ NOVO: Componente que usa o novo sistema de estado
const AppContent = () => {
  const location = useLocation();
  
  // ‚úÖ NOVO: Usar hooks do novo sistema
  const { app, auth, theme } = useGlobalState();
  const { initialize } = useAppInitialization();
  const { savePreference } = useUserPreferences();

  // ‚úÖ NOVO: Comportamento baseado no estado global
  const { trackPageView, trackScrollDepth } = useBehaviorBasedLoading({
    enableBehaviorTracking: true,
    enablePredictiveLoading: true,
    trackUserPreferences: true,
    preloadThreshold: 0.3,
  });

  // ‚úÖ NOVO: Inicializa√ß√£o da aplica√ß√£o
  useEffect(() => {
    // Inicializar sistema de prefetch inteligente
    prefetchRouteModules();
    
    // Pr√©-carregar bibliotecas cr√≠ticas
    preloadCriticalLibs();
    
    // Tracking de m√©tricas de performance
    const metrics = getPrefetchMetrics();
    if (import.meta.env.DEV) {
      console.log('üöÄ Prefetch Metrics:', metrics);
    }
    
    // ‚úÖ NOVO: Inicializar prefer√™ncias e configura√ß√µes
    initialize();
  }, [initialize]);

  // ‚úÖ NOVO: Salvar prefer√™ncias do usu√°rio automaticamente
  useEffect(() => {
    if (auth.user) {
      savePreference('theme', theme.mode);
      // Adicionar mais prefer√™ncias conforme necess√°rio
    }
  }, [auth.user, theme.mode, savePreference]);

  // Tracking de tempo de p√°gina (mantido como estava)
  useEffect(() => {
    const startTime = Date.now();
    const pagePath = window.location.pathname;

    const handlePageUnload = () => {
      const duration = Date.now() - startTime;
      trackPageView(pagePath, duration);
    };

    const handleScroll = () => {
      const scrollDepth = (window.scrollY / (document.body.scrollHeight - window.innerHeight)) * 100;
      trackScrollDepth(pagePath, scrollDepth);
    };

    window.addEventListener('beforeunload', handlePageUnload);
    window.addEventListener('scroll', handleScroll, { passive: true });

    return () => {
      window.removeEventListener('beforeunload', handlePageUnload);
      window.removeEventListener('scroll', handleScroll);
      handlePageUnload();
    };
  }, [trackPageView, trackScrollDepth]);

  // ‚úÖ NOVO: Aplicar tema globalmente
  useEffect(() => {
    // Aplicar classe dark baseada no tema atual
    document.documentElement.classList.toggle('dark', theme.isDark);
  }, [theme.isDark]);

  return (
    <AnimatePresence mode="wait" initial={false}>
      <Routes location={location} key={location.pathname}>
        {/* Rotas p√∫blicas */}
        <Route path="/login" element={renderPublicAuth(<Login />)} />
        <Route path="/forgot-password" element={renderPublicAuth(<ForgotPassword />)} />
        {/* ... outras rotas */}
        
        {/* Rotas protegidas */}
        <Route path="/" element={renderProtected(<Index />)} />
        <Route path="/contratos" element={renderProtected(<Contratos />)} />
        {/* ... outras rotas protegidas */}
        
        {/* Rota 404 */}
        <Route path="*" element={renderPublic(<NotFound />)} />
      </Routes>
    </AnimatePresence>
  );
};

// ‚úÖ NOVO: App principal com sistema unificado
const App = () => {
  return (
    <AppProviders>
      <ToastProvider>
        <TooltipProvider delayDuration={0}>
          <Toaster />
          <Sonner />
          <BrowserRouter>
            <ErrorBoundary>
              <Suspense fallback={<PageLoader />}>
                <AppContent />
              </Suspense>
            </ErrorBoundary>
          </BrowserRouter>
        </TooltipProvider>
      </ToastProvider>
      
      {/* Performance Monitor - apenas em desenvolvimento */}
      {import.meta.env.DEV && (
        <PerformanceMonitor 
          isDevelopment={true}
          position="top-right"
          size="compact"
        />
      )}
    </AppProviders>
  );
};

export default App;

